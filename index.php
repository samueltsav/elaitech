    const track = document.querySelector('.carousel-track');
    const slides = document.querySelectorAll('.projects-card');
    const prevBtn = document.querySelector('.prev');
    const nextBtn = document.querySelector('.next');
    const carousel = document.querySelector('.carousel');
    
    let currentIndex = 0;
    let autoSlideInterval;
    let isPaused = false;

    // Function to update carousel position
    function updateCarousel() {
      const offset = -currentIndex * 100;
      track.style.transform = `translateX(${offset}%)`;
    }

    // Next slide function
    function nextSlide() {
      currentIndex = (currentIndex + 1) % slides.length;
      updateCarousel();
    }

    // Previous slide function
    function prevSlide() {
      currentIndex = (currentIndex - 1 + slides.length) % slides.length;
      updateCarousel();
    }

    // Start auto-slide
    function startAutoSlide() {
      autoSlideInterval = setInterval(nextSlide, 3000); // Slide every 3 seconds
    }

    // Stop auto-slide
    function stopAutoSlide() {
      clearInterval(autoSlideInterval);
    }

    // Event listeners for buttons
    nextBtn.addEventListener('click', () => {
      nextSlide();
      stopAutoSlide();
      startAutoSlide(); // Restart auto-slide after manual interaction
    });

    prevBtn.addEventListener('click', () => {
      prevSlide();
      stopAutoSlide();
      startAutoSlide(); // Restart auto-slide after manual interaction
    });

    // Pause on hover
    carousel.addEventListener('mouseenter', () => {
      isPaused = true;
      stopAutoSlide();
    });

    // Resume on mouse leave
    carousel.addEventListener('mouseleave', () => {
      isPaused = false;
      startAutoSlide();
    });

    // Start auto-slide on page load
    startAutoSlide();
  </script>

  <script>
document.addEventListener('DOMContentLoaded', () => {
  const carousel = document.querySelector('.testimonials-carousel');
  const track = document.querySelector('.testimonials-track');
  const slides = Array.from(track.children);
  let currentIndex = 0;
  let slideWidth = carousel.clientWidth;
  let autoSlideInterval = null;
  const delay = 4000; // ms

  // position slides (not strictly required if using flex + min-width:100%)
  function updateSizes() {
    slideWidth = carousel.clientWidth;
    // ensure exact translate distances
    goTo(currentIndex, false);
  }

  function goTo(index, animate = true) {
    if (!animate) {
      track.style.transition = 'none';
      requestAnimationFrame(() => {
        track.style.transform = `translateX(-${index * slideWidth}px)`;
        // allow transition for subsequent moves
        requestAnimationFrame(() => track.style.transition = 'transform 0.6s ease');
      });
    } else {
      track.style.transform = `translateX(-${index * slideWidth}px)`;
    }
  }

  function next() {
    currentIndex = (currentIndex + 1) % slides.length;
    goTo(currentIndex);
  }
  function prev() {
    currentIndex = (currentIndex - 1 + slides.length) % slides.length;
    goTo(currentIndex);
  }

  // auto slide
  function startAuto() {
    stopAuto();
    autoSlideInterval = setInterval(next, delay);
  }
  function stopAuto() {
    if (autoSlideInterval) { clearInterval(autoSlideInterval); autoSlideInterval = null; }
  }

  // attach optional prev/next buttons if present
  const prevBtn = document.querySelector('.prev');
  const nextBtn = document.querySelector('.next');
  if (prevBtn) prevBtn.addEventListener('click', () => { prev(); startAuto(); });
  if (nextBtn) nextBtn.addEventListener('click', () => { next(); startAuto(); });

  // pause on hover
  carousel.addEventListener('mouseenter', stopAuto);
  carousel.addEventListener('mouseleave', startAuto);

  // responsiveness
  window.addEventListener('resize', () => {
    updateSizes();
  });

  // init
  updateSizes();
  startAuto();
});
</script>

